public class Jogador {
    String posicao;
    String nome;
    int numero;
    String contrato;
    String nacionalidade;

    public Jogador(String posicao, String nome, int numero, String contrato, String nacionalidade) {
        this.posicao = posicao;
        this.nome = nome;
        this.numero = numero;
        this.contrato = contrato;
        this.nacionalidade = nacionalidade;
    }    
}





